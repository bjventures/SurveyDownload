<h1 class="top-heading">Download Survey</h1>
<p>This page demonstrates the delivery a PIEL Survey control file using a web form. You would replace this text with information and
    instructions for participants.</p>

<p>The easiest way of using this form is for the participant to open this page on their mobile device, enter their id and download the survey control
    file directly onto their device. They will be able to choose to open it in the PIEL Survey app.</p>

<p>Note that this example is purely for demonstration purposes. The code is available at an <a href="https://github.com/bjventures/SurveyDownload">this source repository</a>. Feel free to use and modify it.
    For example, you may choose to add verification of entered ids and logging of who downloaded their file. You could even insert other data into
    the file.</p>